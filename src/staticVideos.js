// Helper: parse duration string "H:MM:SS" or "MM:SS" into seconds
function parseDurationToSeconds(duration) {
  if (!duration) return 0;
  const s = String(duration).trim();
  const parts = s.split(':').map((p) => p.replace(/\D/g, ''));
  if (parts.length === 1) {
    return parseInt(parts[0] || '0', 10) || 0;
  } else if (parts.length === 2) {
    const minutes = parseInt(parts[0] || '0', 10) || 0;
    const seconds = parseInt(parts[1] || '0', 10) || 0;
    return minutes * 60 + seconds;
  } else {
    const hours = parseInt(parts[0] || '0', 10) || 0;
    const minutes = parseInt(parts[1] || '0', 10) || 0;
    const seconds = parseInt(parts[2] || '0', 10) || 0;
    return hours * 3600 + minutes * 60 + seconds;
  }
}

// Helper: convert human-readable views like "2.4M" or "1,200" or "33.3K" into number
function parseViewsToNumber(views) {
  if (!views) return 0;
  let s = String(views).toLowerCase().replace(/views?/g, '').trim();
  s = s.replace(/,/g, '');
  const match = s.match(/^([\d.,]+)\s*([km])?$/i);
  if (!match) {
    const digits = s.match(/[\d]+/g);
    return digits ? parseInt(digits.join(''), 10) : 0;
  }
  const num = parseFloat(match[1].replace(/,/g, ''));
  const suffix = match[2] ? match[2].toLowerCase() : null;
  if (!suffix) return Math.round(num);
  if (suffix === 'k') return Math.round(num * 1000);
  if (suffix === 'm') return Math.round(num * 1000 * 1000);
  return Math.round(num);
}

// Helper: parse an uploadDate string into a timestamp (best-effort)
function parseUploadDateToTimestamp(uploadDate) {
  if (!uploadDate) return undefined;
  const relativeKeywords = ['ago', 'day', 'days', 'week', 'weeks', 'month', 'months', 'year', 'years'];
  if (relativeKeywords.some((k) => String(uploadDate).toLowerCase().includes(k))) return undefined;

  const parsed = Date.parse(uploadDate);
  if (!Number.isNaN(parsed)) return parsed;

  try {
    return Number(new Date(uploadDate));
  } catch {
    return undefined;
  }
}

// Normalizer: ensures numeric fields exist for future sorting/filtering
function makeVideo(v) {
  const durationSeconds = parseDurationToSeconds(v.duration);
  const viewsCount = parseViewsToNumber(v.views);
  const uploadTimestamp = parseUploadDateToTimestamp(v.uploadDate);
  return {
    ...v,
    durationSeconds,
    viewsCount,
    uploadTimestamp,
  };
}

